<script>
    document.addEventListener('DOMContentLoaded', function() {
        var table = document.getElementById('editableTable');
        var cells = table.getElementsByTagName('td');

        for (var i = 0; i < cells.length; i++) {
            cells[i].setAttribute('contenteditable', 'true');
        }
    });


    function addImage() {
        var input = document.getElementById('imageInput');
        var container = document.getElementById('imageContainer');

        if (input.files && input.files[0]) {
            var reader = new FileReader();

            reader.onload = function(e) {
                var image = document.createElement('img');
                image.src = e.target.result;


                image.style.width = '150px';
                image.style.height = '150px';

                container.innerHTML = '';
                container.appendChild(image);
            };

            reader.readAsDataURL(input.files[0]);
        }
    }


    function saveTableValues() {
    var tableValues = {};

    // Loop through each row in the table
    $('#editableTable tbody tr').each(function (rowIndex) {
        var rowData = [];

        // Loop through each cell in the row
        $(this).find('td').each(function (cellIndex) {
            // Add cell value to rowData array
            rowData.push($(this).text());
        });

        // Add rowData array to tableValues object
        tableValues['row_' + rowIndex] = rowData;
    });

    // Send data to the server
    $.ajax({
        type: 'POST',
        url: 'save_data.php',
        data: {
            tableValues: JSON.stringify(tableValues)
        },
        success: function (response) {
            // Use SweetAlert2 for success message
            Swal.fire({
                icon: 'success',
                title: 'Success!',
                text: 'Table values saved successfully!',
            }).then(function () {
                // Redirect to index page
                window.location.href = 'index.php'; // Update with your actual index page URL
            });
        },
        error: function (error) {
            console.error('Error saving table values:', error);
        }
    });
}

